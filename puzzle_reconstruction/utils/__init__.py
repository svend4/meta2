"""
Утилиты: логирование, геометрия, кэш дескрипторов, ввод-вывод.

Модули:
    logger     — структурированное логирование с цветами (get_logger, stage, PipelineTimer)
    geometry   — геометрические операции (rotation_matrix_2d, polygon_area, poly_iou, …)
    cache      — LRU-кэш и дисковый кэш дескрипторов (DescriptorCache, DiskCache, @cached)
    io         — загрузка/сохранение фрагментов и Assembly (load_image_dir, save_assembly_json, …)
    profiler   — профилировщик шагов пайплайна (StepProfile, PipelineProfiler, @timed)
    visualizer — утилиты визуализации (word boxes, contours, matches, confidence bar)
    metrics    — метрики качества восстановления (ReconstructionMetrics, IoU, Kendall τ,
                 permutation distance, precision/recall, placement accuracy)
    image_stats    — статистический анализ изображений (ImageStats, энтропия, резкость,
                     гистограммные моменты, градиентные статистики, compare_images)
    patch_extractor — извлечение патчей (Patch, PatchSet, grid, sliding, random, border)
    image_io        — загрузка/сохранение (ImageRecord, load_image, save_image,
                      load_directory, filter_by_extension, parse_fragment_id,
                      resize_to_max, batch_resize)
    transform_utils — геометрические преобразования (rotate, flip, scale, crop_region,
                      affine_from_params, compose_affines, apply_affine,
                      apply_homography, batch_rotate)
    color_utils     — цветовые утилиты (to_gray, to_lab, to_hsv, from_lab,
                      compute_histogram, compare_histograms, dominant_colors,
                      color_distance, strip_histogram)
    mask_utils      — утилиты масок (create_alpha_mask, apply_mask, erode_mask,
                      dilate_mask, mask_from_contour, combine_masks,
                      crop_to_mask, invert_mask)
    array_utils     — утилиты numpy-массивов (normalize_array, pad_to_shape,
                      crop_center, stack_arrays, chunk_array, sliding_window,
                      flatten_images, unflatten_images, compute_pairwise_norms)
    contour_utils   — утилиты контуров (simplify_contour, interpolate_contour,
                      contour_area, contour_perimeter, contour_bbox,
                      contour_centroid, contour_iou, align_contour_orientation,
                      contours_to_mask, mask_to_contour)
    histogram_utils — утилиты гистограмм (compute_1d_histogram, compute_2d_histogram,
                      histogram_equalization, histogram_specification,
                      earth_mover_distance, chi_squared_distance,
                      histogram_intersection, backproject, joint_histogram)
    keypoint_utils  — ключевые точки и дескрипторы (KeypointSet, detect_keypoints,
                      filter_by_response, filter_by_region, describe_keypoints,
                      match_descriptors, filter_matches_ransac, keypoints_to_array,
                      array_to_keypoints, compute_match_score)
    signal_utils    — 1D обработка сигналов (smooth_signal, normalize_signal,
                      find_peaks, find_valleys, compute_autocorrelation,
                      compute_cross_correlation, signal_energy, segment_signal,
                      resample_signal, phase_shift)
    bbox_utils      — утилиты ограничивающих прямоугольников (BBox, bbox_iou,
                      bbox_intersection, bbox_union, expand_bbox, crop_image,
                      bboxes_from_mask, merge_overlapping_bboxes,
                      bbox_center, bbox_aspect_ratio)
    sparse_utils    — утилиты разреженных матриц оценок (SparseEntry,
                      to_sparse_entries, from_sparse_entries, sparse_top_k,
                      threshold_matrix, symmetrize_matrix, normalize_matrix,
                      diagonal_zeros, matrix_sparsity, top_k_per_row)
    clustering_utils — кластеризация фрагментов (ClusterResult, kmeans_cluster,
                       assign_to_clusters, compute_inertia, silhouette_score_approx,
                       hierarchical_cluster, find_optimal_k, cluster_indices,
                       merge_clusters)
    distance_utils  — метрики расстояния и сходства (euclidean_distance,
                      cosine_distance, cosine_similarity, manhattan_distance,
                      chebyshev_distance, hausdorff_distance, chamfer_distance,
                      normalized_distance, pairwise_distances, nearest_neighbor_dist)
    smoothing_utils — сглаживание сигналов и контуров (SmoothingParams,
                      moving_average, gaussian_smooth, median_smooth,
                      exponential_smooth, savgol_smooth, smooth_contour,
                      apply_smoothing, batch_smooth)
    feature_selector — выбор признаков (FeatureSet, SelectionResult,
                       variance_selection, correlation_selection, rank_features,
                       pca_reduce, normalize_features, select_top_k,
                       apply_selection, batch_select)
    graph_utils      — граф совместимости фрагментов (GraphEdge, FragmentGraph,
                       build_graph, dijkstra, shortest_path, minimum_spanning_tree,
                       connected_components, node_degrees, subgraph, batch_build_graphs)
    cache_manager    — LRU-кэш промежуточных результатов (CacheEntry, CacheStats,
                       LRUCache, make_cache, cached_call, merge_caches)
    progress_tracker — трекер прогресса пайплайна (StepRecord, PipelineReport,
                       ProgressTracker, make_tracker, run_step, summarize_tracker)
    config_manager   — управление конфигурацией (ConfigField, ConfigSpec,
                       ConfigSnapshot, validate_field_type, validate_config,
                       load_config, merge_configs, diff_configs,
                       make_config_snapshot, batch_validate)
    result_cache     — кэш промежуточных результатов с TTL (CachePolicy, CacheRecord,
                       CacheSummary, ResultCache, make_cache, cached_result,
                       merge_caches, evict_expired)
    metric_tracker   — отслеживание метрик (MetricRecord, MetricStats, TrackerConfig,
                       MetricTracker, make_tracker, merge_trackers,
                       compute_moving_average, export_metrics)
    event_log        — журнал событий пайплайна (EventLogConfig, EventRecord,
                       EventSummary, EventLog, make_event_log, log_event,
                       filter_events, summarize_events, merge_event_logs,
                       export_event_log)
    batch_processor  — пакетная обработка элементов (ProcessConfig, ProcessItem,
                       BatchSummary, make_processor, process_items,
                       filter_successful, retry_failed_items, split_batch,
                       merge_batch_results)
    event_bus        — шина событий pub/sub (BusConfig, EventRecord, BusSummary,
                       EventBus, make_event_bus, collect_events, drain_bus)
    pipeline_runner  — запуск многоэтапного пайплайна (RunnerConfig, StepResult,
                       PipelineResult, PipelineStep, make_step, run_pipeline,
                       get_step_output, filter_step_results, retry_failed_steps)
    polygon_utils    — геометрия полигонов (polygon_area, polygon_perimeter,
                       polygon_centroid, point_in_polygon, convex_hull,
                       polygon_bounding_box, polygon_aspect_ratio,
                       translate_polygon, scale_polygon, rotate_polygon,
                       polygon_similarity)
    score_aggregator — агрегация оценок совместимости (AggregationMethod,
                       ScoreVector, AggregationResult, weighted_sum,
                       harmonic_mean, geometric_mean, aggregate_pair,
                       aggregate_matrix, top_k_pairs, batch_aggregate)
"""

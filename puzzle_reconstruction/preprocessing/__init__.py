"""
Предобработка фрагментов документа.

Модули:
    segmentation    — выделение маски фрагмента (Otsu / Adaptive / GrabCut)
    contour         — извлечение и нормализация внешнего контура
    orientation     — оценка и коррекция угла поворота
    color_norm      — нормализация цвета (CLAHE, Gray World, гамма)
    denoise         — шумоподавление (Gaussian, Median, Bilateral, NLM, auto)
    augment         — аугментация данных (crop, rotate, noise, jitter, JPEG)
    edge_detector   — специализированное детектирование краёв (Canny, Sobel, LoG)
    skew_correction — коррекция наклона (Hough, projection, FFT)
    perspective     — коррекция перспективных искажений (contour, Hough)
    noise_reduction — расширенное шумоподавление (DenoiseResult, NLM, bilateral,
                      morphological, smart_denoise, batch_denoise)
    contrast        — улучшение контраста (ContrastResult, CLAHE, histeq,
                      gamma, stretch, retinex, auto_enhance, batch_enhance)
    binarizer       — бинаризация изображений (BinarizeResult, Otsu, Sauvola,
                      Niblack, Bernsen, adaptive, auto_binarize, batch_binarize)
    document_cleaner — очистка документов (тени, рамки, освещённость, пятна)
    noise_analyzer   — анализ шума (σ, SNR, JPEG-артефакты, зернистость, качество)
    fragment_cropper — обрезка к содержательной области (CropResult, auto_crop,
                       find_content_bbox, pad_image, crop_to_content, batch_crop)
    noise_reducer    — лёгкое шумоподавление (NoiseReductionResult, estimate_noise,
                       gaussian_reduce, median_reduce, bilateral_reduce, auto_reduce,
                       batch_reduce)
    deskewer         — коррекция наклона (DeskewResult, estimate_skew_projection,
                       estimate_skew_hough, deskew_image, auto_deskew, batch_deskew)
    background_remover — удаление фона (BackgroundRemovalResult, remove_background_thresh,
                         remove_background_edges, remove_background_grabcut,
                         auto_remove_background, batch_remove_background)
    patch_normalizer   — нормализация патчей (NormalizationParams, equalize_histogram,
                         stretch_contrast, standardize_patch, normalize_patch,
                         batch_normalize, compute_normalization_stats)
    quality_assessor        — оценка качества изображений (QualityReport, estimate_blur,
                              estimate_noise, estimate_contrast, estimate_completeness,
                              assess_quality, filter_by_quality, batch_assess_quality)
    illumination_corrector  — коррекция освещённости (IlluminationParams,
                              estimate_background, subtract_background,
                              correct_by_homomorph, correct_by_retinex,
                              correct_illumination, batch_correct, estimate_uniformity)
    morphology_ops          — морфологические операции (MorphParams, erode, dilate,
                              open_morph, close_morph, tophat, blackhat,
                              morphological_gradient, skeleton, remove_small_blobs,
                              fill_holes, apply_morph, batch_morph)
    frequency_filter        — фильтрация в частотной области (FrequencyFilterParams,
                              fft_image, ifft_image, gaussian_low_pass,
                              gaussian_high_pass, band_pass_filter, notch_filter,
                              apply_frequency_filter, batch_frequency_filter)
    channel_splitter        — разделение и обработка каналов (ChannelStats,
                              split_channels, merge_channels, channel_statistics,
                              equalize_channel, normalize_channel,
                              channel_difference, apply_per_channel, batch_split)
    adaptive_threshold      — адаптивная бинаризация (ThresholdParams,
                              global_threshold, adaptive_mean, adaptive_gaussian,
                              niblack_threshold, sauvola_threshold,
                              bernsen_threshold, apply_threshold, batch_threshold)
    noise_filter            — фильтрация шума (NoiseFilterParams, average_filter,
                              gaussian_filter, median_filter, bilateral_filter,
                              nlm_filter, apply_noise_filter, batch_noise_filter)
    edge_enhancer           — усиление краёв (EdgeEnhanceParams, unsharp_mask,
                              laplacian_enhance, hybrid_enhance,
                              gradient_scale_enhance, sharpness_measure,
                              apply_edge_enhance, batch_edge_enhance)
    color_normalizer        — нормализация цвета (NormConfig, NormResult,
                              gamma_correction, equalize_histogram, apply_clahe,
                              grey_world_balance, max_rgb_balance, minmax_normalize,
                              normalize_image, batch_normalize)
    image_enhancer          — улучшение качества изображений (EnhanceConfig,
                              EnhanceResult, sharpen_image, denoise_image,
                              enhance_contrast, enhance_image, batch_enhance)
    gradient_analyzer       — анализ градиентов (GradientConfig, GradientMap,
                              GradientProfile, compute_gradient_map,
                              compute_orientation_histogram,
                              extract_gradient_profile, compare_gradient_profiles,
                              batch_extract_gradient_profiles)
"""
